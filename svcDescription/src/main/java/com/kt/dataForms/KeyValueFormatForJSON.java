package com.kt.dataForms;

public class KeyValueFormatForJSON {
	
	String key;
	String value;
	
	public void setKey (String key) {
		this.key = key;
	}
	
	public String getKey () {
		return this.key;
	}
	
	public void setValue (String value) {
		this.value = value;
	}
	
	public String getValue () {
		return this.value;
	}

}
